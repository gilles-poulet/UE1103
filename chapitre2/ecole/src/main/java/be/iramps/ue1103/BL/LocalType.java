package be.iramps.ue1103.BL;

/**
 * Enumérateur définissant les types de locaux possibles
 * au sein d'un établissement
 * {@link #STD}
 * {@link #INF}
 */
public enum LocalType {
    /**
     * Local standard, sans équipement
     * particulier
     */
    STD,

    /**
     * Laboratoire informatique possédant
     * des ordinateurs
     */
    INF
}
